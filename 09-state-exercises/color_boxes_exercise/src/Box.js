  constructor(props) {
    super(props);

    this.genColor = currentColor => {
      let colorRange = this.props.colors;

      if (currentColor) {
        colorRange = this.props.colors.filter(color => color !== currentColor);
      }

      return colorRange[Math.floor(Math.random() * colorRange.length)];
    };

    this.state = {
      currentColor: this.genColor(null)
    };
  }

  handleClick(e) {
    const currentColor = e.target.style.backgroundColor;
    this.setState({
      currentColor: this.genColor(currentColor)
    });
  }

  render() {
    return (
      <div
        className="Box"
        onClick={e => {
          this.handleClick(e);
        }}
        style={{ background: this.state.currentColor }}
      />
    );
  }
